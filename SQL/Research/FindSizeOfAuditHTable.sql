-- Create a counter to use in insert statement
IF OBJECT_ID('SEED.seqAudit') IS NOT NULL
BEGIN
	PRINT 'Dropping sequence SEED.seqAudit';
	DROP SEQUENCE SEED.seqAudit;
END
CREATE SEQUENCE SEED.seqAudit AS INT START WITH 50000 INCREMENT BY 1 MINVALUE 1 MAXVALUE 2000000000;

-- Check row count
DECLARE @rcnt BIGINT;
SET @rcnt = (SELECT COUNT(*) FROM SEED.AUDITH);

WHILE @rcnt <= 1000000
BEGIN
	INSERT INTO SEED.AUDITH
	SELECT        
		(NEXT VALUE FOR SEED.seqAudit), 
		TBL_0, DAT_0, HOU_0, EVT_0, ID1_0, ID2_0, ADOUSR_0, 
		SYSUSR_0, ADRCLI_0, STA_0, STABI_0, NUM1_0, NUM2_0, 
		NUM3_0, NUM4_0, NUM5_0, NUM6_0, NUM7_0, NUM8_0, 
		NUM9_0, NUM10_0, NUM11_0, NUM12_0, NUM13_0, NUM14_0, 
		NUM15_0, NUM16_0, CHAR1_0, CHAR2_0, CHAR3_0, CHAR4_0, CHAR5_0, 
		CHAR6_0, CHAR7_0, CHAR8_0, CHAR9_0, CHAR10_0, 
		CHAR11_0, CHAR12_0, CHAR13_0, CHAR14_0, CHAR15_0, CHAR16_0
	FROM SEED.AUDITH 
		CROSS JOIN sys.columns c
	WHERE ROWID = 146;

	SET @rcnt = (SELECT COUNT(*) FROM SEED.AUDITH);
END
GO
sp_spaceused 'SEED.AUDITH';

